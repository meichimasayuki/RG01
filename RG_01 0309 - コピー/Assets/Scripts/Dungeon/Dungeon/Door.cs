public class Door
{
    private int _x;
    private int _z;
    private int _dir;
    private int _index;
    public int X
    {
        get { return _x; }
        set { _x = value; }
    }
    public int Z
    {
        get { return _z; }
        set { _z = value; }
    }
    public int Dir
    {
        get { return _dir; }
        set { _dir = value; }
    }
    public int Index
    {
        get { return _index; }
        set { _index = value; }
    }
}
